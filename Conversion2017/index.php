</ul>
<p>You will then be prompted to complete your identifying account information. Once your account is recognized, you will be prompted to create a password that meets the security requirements of our new online banking platform. You will then be prompted to set up your security questions. There are numerous options of questions for you to choose from. You must specify three questions to use. Your answers to each question must be at least six characters with zero spaces. If your answer to one of the questions is not six characters long, you can either choose a different question or repeat your answer twice (with zero spaces). (Ex: What is your favorite color? redred) All answers are case sensitive. </p>
