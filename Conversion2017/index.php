  <li>Your current Timely Teller pins will not convert to the new system. Once Timely Teller becomes available post-conversion, you will need to log in using your Member Account Number as the user and the last four digits of the primary account holder&rsquo;s SSN as the password. (Ex: User = 12345 and Password = 6789) Following your first log in, you must change your log in credentials.</li>
