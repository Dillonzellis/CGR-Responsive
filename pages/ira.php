<h1 class="page-title">Individual Retirement Accounts</h1>

<p>Increase the value of your income and protect your future with an IRA.  An IRA Share Account can be opened with just $25, and you can make periodic deposits throughout the year.</p>

<p class="df-stitle fw600">Traditional IRA</p>

<p>Individual retirement accounts are a smart way to save for the future. A traditional IRA can be opened and funded without any employer participation. Contributions and/or earnings are tax-deferred until retirement. Unlike many employer plans, money in the account is always accessible; however, until age 59 ½ there is a 10% early distribution penalty unless you qualify for an exemption due to one of the following: disability, qualifying education expenses, unemployment, qualifying first-home purchases, death, or receipt of your IRA assets in equal payments over your life expectancy.</p>

<p>According to the IRS Regulations, members under age 70 ½ may contribute to an IRA.</p>
 
<p class="df-stitle fw600">Roth IRA</p>

<p>Roth IRAs differ from Traditional IRAs in that the money you contribute to a Roth IRA has already been taxed. So the principal amount is never subject to taxes or penalties in the future, as long as you stay within the contribution guidelines. This retirement plan allows the money you contribute to grow tax-deferred.</p>

<p>If you do not withdraw any of the earnings until you have had the plan for at least five (5) years, or satisfy one of the qualifying events, those tax-deferred earnings become tax free. Unlike the traditional IRA, there is no 70 ½ age limit on making contributions. You simply need to have earned income equal to the amount you contribute up to the maximum amount set by the IRS each year.</p>
 

