<h1 class="page-title">Who We Are</h1>

<p>CGR Credit Union is a not-for-profit, member-owned and -operated, State chartered financial services organization. Founded by hospital employees in 1971, CGR Credit Union has served over 10,000 healthcare employees, professionals, members and their families. </p>

<p>On the surface we may look like any other full-service community credit union or commercial bank but we are not open to the general public. Our primary membership is professionals and employees within the medical community located in Bibb, Houston, Baldwin, Peach, Tift, Monroe, and Putnam Counties in Georgia. While hospital employees do make up the largest portion of our very active membership, many of our members work in or for the area’s doctor’s offices, clinics, nursing homes, and other healthcare related businesses.  Family members and persons living in the same household of these eligible employee groups can also be members and enjoy the full use and benefits of the Credit Union.  Under our Charter, once you become a member you are a member for life.</p>

<p>Unlike commercial banks and most others in the financial services business, CGR Credit Union is not a for-profit stockholder owned company. Our owners are our members and we serve only them. All of our volunteer officials, board of directors and committees, are healthcare employees both active and retired.</p>

<p>CGR Credit Union is financially sound and offers the utmost security to our members. Accounts are federally insured up to $250,000 through the National Credit Union Administration, a U.S. government agency.</p>

