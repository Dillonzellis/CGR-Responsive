<h1 class="page-title">Fraud can happen to anyone—and it’s becoming more common.</h1>


<p>We’ve heard from members recently of calls with individuals claiming to be from the credit union.</p>

<p>Scam attempts that play on your emotions and seek to gain your trust are becoming more frequent and more effective.</p>

<p>CGR will never contact you and ask for personal information by text, email, or an unsolicited phone call. This includes unsolicited calls that ask you to obtain or provide a password or challenge question answers.</p>


<h2 class="page-title-1"><strong>How scammers may contact you</strong></h2>

<p>Their messages can sound urgent, seem familiar, often be poorly worded or contain other errors, and even use your personal information to convince you they’re legitimate.</p>

<p>Scammers can even pose as CGR—you may see “CGR Credit Union” appear as an email sender’s name, in the caller or text ID, and the telephone number displayed may be an actual CGR number.</p>

