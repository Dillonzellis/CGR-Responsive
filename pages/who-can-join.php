<h1 class="page-title">Who Can Join?</h1>

<p>CGR Credit Union is a Georgia based credit union serving healthcare professionals and employees in Bibb, Houston, Baldwin, Peach, Tift, Monroe, and Putnam counties. If you are currently employed by, associated with, or retired from, the medical field and live or work in one of these counties you may be eligible to take full advantage of membership in CGR Credit Union.  </p>

<p>Membership is also a family affair. Share the Credit Union's benefits and financial opportunities with members of your immediate family. Anyone related to you by blood, adoption or marriage or living in the same household is eligible for membership.
