<h1 class="page-title">Transaction Monitoring</h1>

<p>We’ll let you know if there are any suspicious transactions on your account.  We recommend being proactive checking your account as well.</p>
<p>Important Tips to Help Prevent Debit Card Fraud:</p>

<ul>
<li>Check your monthly statements immediately.  Make sure all payments are yours.  </li>
<li>Periodically check your account balance and transactions, by utilizing online banking, by telephone, or by printing interim statements at the ATM.</li>   
<li>Contact the credit union immediately if your card is lost, stolen or subject to fraudulent use.  </li>
<li>Keep a record of card numbers, PINs, expiration dates and 1-800 numbers for financial institutions so you can contact the issuer easily in cases of theft.</li>  
<li>Memorize your PIN number.  Do not use your birth date, address, phone number or social security number.  Never store your PIN with your card, and do not make it available to others.  </li>
<li>Keep your receipts.  You'll need them to check your statement.  If they have your account number on them, tear up or shred receipts before throwing them away.  </li>
<li>Mark through any blank spaces on debit slips, including the tip line at restaurants, so the total amount cannot be changed.  </li>
<li>Know your limits.  Many issuers limit daily purchases and withdrawals for your protection.</li>  
<li>Do not use an ATM if it looks suspicious, it could be a skimming device.  </li>
<li>Be wary of those trying to help you, especially when an ATM "eats" your card, they may be trying to steal your card number and PIN. </li> 
</ul>

<p>Do not give your PIN number to anyone over the phone, often thieves steal the cards and then call the victim for their PIN, sometimes claiming to be law enforcement or the issuing bank. </p>

<p>If you are travelling - is your account ready to take you there?</p>

<p>If you plan on travelling overseas:</p>

<ol>
<li><strong>Let CGR Credit Union know when you’ll be traveling outside the USA</strong><br/>
As a security precaution, if a credit or debit card charge occurs someplace out of the ordinary, your card may be flagged and blocked.</li>

<li><strong>Double check your account balance, credit limits, and card expiration dates</strong></li>

<li><strong>Carry a variety of payment options</strong><br/>
In addition to your debit and primary credit card, it’s a good idea to have a backup card and a small amount of cash carried separately from the others</li>

<li>Make note of issuer's contact number</li>
</ol>

<p>Before you leave, make a list of the issuer’s phone number for each card. This way, if your cards are lost or stolen, you have their contact information to immediately report the loss</p>

