<h1 class="page-title">Beware the Make-A-Wish Charity Scam!</h1>

<div class="twelve columns">

<div class="six columns">
<p>Sadly, hundreds of crooks hide behind the veil of charities. They'll impersonate known charities or create a bogus one, then solicit funds and pocket the cash.</p>
<p>Most recently, scammers have abused the name of the Make-A-Wish Foundation, an organization dedicated to granting the most longed-forwishes of terminally ill children. </p>
  
</div>

<div class="five columns">
<p align="center"><img src="../images/pages/scam.jpg" alt="Scam Warning!" width="417" height="278"></p>
</div>

</div>

<p>Here's how it works.</p>
<p>The scammer calls the victim and announces they've won hundreds of thousands of dollars in an alleged sweepstakes conducted by Make-A-Wish. The caller claims to be representing the FTC or another federal institution. The &quot;government official&quot; then explains that the &quot;winner&quot; must pay thousands of dollars for taxes and insurance to claim their winnings.</p>
<p>Of course, there is no sweepstakes and the caller is no government official. In fact, on its website, Make-A-Wish asserts that it never conducts sweepstakes. If you wire your money over, you'll never hear from the caller again.</p>
<p>There are several red flags here.</p>
<p>First, the FTC does not hand out sweepstakes prizes. Second, you should never have to pay money to claim a prize. And third, no legitimate organization will ask for such large amounts of money to be paid over the phone.</p>
<p>If you've been contacted, report the scam immediately at <a href="" data-remodal-target="scam1" title="Scam Resource">FTC.gov</a>. Next, notify Make-A-Wish by email at <a href="mailto:FraudAlerts@wish.org">FraudAlerts@wish.org</a>.</p>
<p>Unfortunately, this scam is not the first of its kind and it won't be the last. Here's how to verify that you're only donating to legitimate charities:</p>
<p><strong>1.) Don't donate over the phone</strong></p>
<p>It's hard to determine authenticity over the phone, and up to 95 cents of every donated dollar may actually go to the telemarketer.</p>
<p><strong>2.) Be wary of sob stories</strong></p>
<p>When a caller preys on your heart strings to the point of discomfort, you may be falling for a scam.</p>
<p><strong>3.) Donate with caution after catastrophe</strong></p>
<p>Natural disasters bring out the generosity in people, but they also bring out the bogus charities. When disaster strikes, you can still help. Just make sure your money is going to larger, recognizable organizations. Also, you can contact the charity yourself to be certain you've reached the real entity.</p>
<p><strong>4.) Know the charity</strong></p>
<p>When choosing a charity, research it first. This way, when someone impersonates this organization while collecting for a cause you know they don't support, you'll recognize the scam.</p>
<p><strong>5.) Read reviews</strong></p>
<p>Check for a charity's legitimacy on objective review sites like CharityNavigator and CharityWatch.</p>
<p><strong>6.) Ask for info</strong></p>
<p>If a caller sounds genuine and you'd like to donate over the phone, first ask for details about the charity. If the caller is hesitant to answer or unsure in their response, hang up!</p>
<p><strong>7.) Give safely</strong></p>
<p>Never wire money to an unverified recipient. Similarly, only provide sensitive information if you're certain the caller is genuine. If you're in doubt, contact the organization on your own to donate funds.</p>
