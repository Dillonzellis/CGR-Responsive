<p><strong>Back-To-School Savings</strong></p>
  <p><strong>Q:</strong> Back-to-school shopping kills my budget each year. Is there any way to start the year off right...without spending a small fortune?</p>
  <p><strong>A:</strong> Rows of freshly sharpened pencils, an unopened box of crayons and a pair of shiny, new shoes will provide a thrill for any child.</p>
  
