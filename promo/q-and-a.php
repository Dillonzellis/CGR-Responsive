<p>As the parent, though, you're the one footing the bill. If the thought of all that back-to-school spending makes your head pound, you're not alone. The National Retail Federation reports that parents of children entering kindergarten through 12th grade plan to spend nearly $700 per child on school supplies, new clothing and shoes this season. That's enough to fill any budgeting parent with dread.</p>
  <p>No worries - as always, CGR Credit Union has your back! Let us help you navigate the second-largest shopping season of the year with your budget and sanity intact. Read on for 12 ways to save on back-to-school shopping.</p>
  <p><strong>1.) Do a house-wide sweep</strong></p>
  <p>Before you spend a penny on new supplies or clothing, scour your closets and drawers to see what you have lying around the house. Round up all the supplies you find and take inventory. Write it all down and keep the list handy - in your phone, purse or car - so you don't forget what you already have and end buying unnecessary items.</p>
  <p>While digging through your kids' closets, sort and purge. Donate outgrown clothing (take note and get a receipt for tax deduction purposes) and throw out everything that isn't wearable. This way, their closets won't be cluttered with junk and you'll know exactly what you have and what each child still needs.</p>
  <p><strong>2.) Get the teacher-approved list</strong></p>
  <p>Most schools and teachers will send you a school supply list that details exactly what your child will need. Those lists are also often available at major retailers. Pay close attention to specifics on the list instead of buying supplies at random. This will prevent you from buying items your child can't use and being forced to later repurchase according to teacher criteria.</p>
  <p><strong>3.) Spread your purchases</strong></p>
  <p>Spreading your back-to-school purchases throughout the summer will allow you to take advantage of weekly sales. One week there might be a great deal on pencils, the next week, folders will be dirt cheap. Over the course of the summer, you'll get all your supplies at terrific prices. Also, by picking up a few items a week, you won't feel the financial squeeze as much as you would if you'd buy everything at once.</p>
  <p><strong>4.) Take advantage of sales-tax holidays</strong></p>
  <p>Many states have a sales-tax holiday during the first week of August; others have tax-free days in July. Look up the timing of your state's gift to budgeting parents and do your shopping then. You'll save big!</p>
  <p><strong>5.) Organize a clothing swap</strong></p>
  <p>The clothing your best friend is trashing may be the perfect fit for your daughter. Organize a clothing swap party with other parents in your area. Choose a date and venue, and instruct all attendants to show up with three or more items of gently used children's clothing. At the party, parents can exchange their kids' outgrown clothing and go home with incredible finds - all free of charge!</p>
  <p><strong>6.) Find the best prices</strong></p>
  <p>This doesn't have to mean touring your town in search of the best deal on crayons. Instead, hunt for specials in the Sunday paper and weekly circulars and look up coupons and deals online, at sites like RetailMeNot and CouponCabin. To make it even easier, check out <a href="" data-remodal-target="pricegrabber" title="PriceGrabber.com">PriceGrabber.com</a> or use the ShopSavvy app for help in snagging the lowest possible price on an item.</p>
  <p><strong>7.) Use Twitter and Facebook to save</strong></p>
  <p>Many companies will send coupon links to their followers and let them know about upcoming sales. Monitor your favorite stores' Twitter feeds and Facebook updates to find super deals.</p>
  <p>Follow these stores on Twitter:<br>
    Staples: @StaplesTweets<br>
    Office Max:@OfficeMaxDeal<br>
    TJ Maxx:@tjmaxx<br>
  Marshalls:@marshalls</p>
  <p><strong>8.) Save through Ebates</strong></p>
  <p>When you buy through Ebates, you earn cash back on every purchase. You'll also find exclusive deals and offers on the site. You can shop major brands and stores like Macy's, Walmart, and Kohl's on Ebates. Some users receive upward of $300 from the site throughout the year. That's like getting paid to shop!</p>
  <p><strong>9.) Time it right</strong></p>
  <p>Your child needs to be ready for the first day of school - they don't need a year's supply of paper or a full autumn wardrobe before Labor Day. Purchase what your kids need now, and save the rest for later. You'll find deep discounts on school supplies and fall clothing just a few weeks into the school year.</p>
  <p><strong>10.) Set limits</strong></p>
  <p>Every year there's a must-have school supply or clothing trend. You want your child to fit in, but you don't have a money tree growing out back!</p>
  <p>That's why it's important to set limits. Share your budget with your child. If your budget allows, let them choose one or two pricier items - but that's it! Don't give in to every whim; you'll be enforcing bad habits and breaking the budget at the same time. If your child insists on more, you can always check out sites like eBay and Craigslist for discounted high-end items.</p>
  <p><strong>11.) Cash and debit card only!</strong></p>
  <p>Paying with cash or using a debit card that draws from your checking account will help you stick to your budget. Resist the urge to charge supplies if you can. You don't want to end up paying interest on pencils for months after they've already broken.</p>
  <p><strong>12.) Plan ahead</strong></p>
  <p>Finally, start thinking about the next school year now! When school supplies and backpacks are ridiculously discounted a few weeks into the school year, stock up for next year. Stash away your extra supplies to pull out at the end of next summer. You'll be grateful you did!</p>
